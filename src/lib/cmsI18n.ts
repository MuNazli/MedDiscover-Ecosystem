export type CmsUiKey = keyof typeof cmsUiDictionary.de;

export function isCmsUiLocale(value: string | null | undefined): value is CmsUiLocale {
  return CMS_UI_LOCALES.includes(value as CmsUiLocale);
}

export function t(
  locale: CmsUiLocale,
  key: CmsUiKey,
  vars?: Record<string, string | number>
) {
  const base = cmsUiDictionary[locale] ?? cmsUiDictionary.de;
  let text = base[key] ?? cmsUiDictionary.de[key];
  if (!vars) {
    return text;
  }
  Object.entries(vars).forEach(([k, v]) => {
    text = text.replaceAll(`{${k}}`, String(v));
  });
  return text;
}
